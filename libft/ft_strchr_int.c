#include "libft.h"

int			ft_strchr_int(char *str, int ch)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i + 1] == '\0' && (char)ch == '\0')
			return (0);
		if (str[i] == ch)
			return (i);
		i++;
	}
	return (0);
}
