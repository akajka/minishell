#include "libft.h"

char			*ft_strncpy(char *dest, const char *src, size_t n)
{
	char		*c;

	c = dest;
	while (n > 0 && *src)
	{
		n--;
		*c++ = *src++;
	}
	while (n > 0)
	{
		n--;
		*c++ = '\0';
	}
	return (dest);
}
